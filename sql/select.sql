SELECT * FROM (
    SELECT * FROM WORDS_COCA WHERE COCA IS NOT NULL    -- FOR WORDS
    UNION ALL
    SELECT * FROM (
        SELECT * FROM WORDS_COCA WHERE COCA IS NULL AND word <> '-' ORDER BY word)    -- FOR PHRASE put similar phrases together
    UNION ALL
    SELECT * FROM WORDS_COCA WHERE COCA IS NULL AND word = '-')    -- FOR SENTENCE
